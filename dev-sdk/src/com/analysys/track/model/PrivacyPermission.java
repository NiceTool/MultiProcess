package com.analysys.track.model;

import java.io.Serializable;

/**
 * 是否同意隐私权限
 */
public class PrivacyPermission implements Serializable {

    private static final long serialVersionUID = 1L;
    /**
     * 应用SDK控制 0,1
     */
    private String AValid;
    /**
     * 设备SDK控制 0,1
     */
    private String DValid;

    public String getAValid() {
        return AValid;
    }

    public String getDValid() {
        return DValid;
    }

    public void setAValid(String AValid) {
        this.AValid = AValid;
    }

    public void setDValid(String DValid) {
        this.DValid = DValid;
    }
}
